package com.duospace.duospace.rmres;

public class Rmres {
	private int rmresNum, roomCode, reservNum, people, memberNum;
	private String startDate, endDate, userName, tel, signSpot;
	public int getMemberNum() {
		return memberNum;
	}
	public void setMemberNum(int memberNum) {
		this.memberNum = memberNum;
	}
	public String getSignSpot() {
		return signSpot;
	}
	public void setSignSpot(String signSpot) {
		this.signSpot = signSpot;
	}
	public int getRmresNum() {
		return rmresNum;
	}
	public void setRmresNum(int rmresNum) {
		this.rmresNum = rmresNum;
	}
	public int getRoomCode() {
		return roomCode;
	}
	public void setRoomCode(int roomCode) {
		this.roomCode = roomCode;
	}
	public int getReservNum() {
		return reservNum;
	}
	public void setReservNum(int reservNum) {
		this.reservNum = reservNum;
	}
	public int getPeople() {
		return people;
	}
	public void setPeople(int people) {
		this.people = people;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
}
