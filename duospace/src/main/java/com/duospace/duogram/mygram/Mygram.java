package com.duospace.duogram.mygram;

public class Mygram {
	private int memberNum;
	private String intro, coversavefilename, profilesavefilename, company, school;
	private String home, created;
	
	public int getMemberNum() {
		return memberNum;
	}
	public void setMemberNum(int memberNum) {
		this.memberNum = memberNum;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getCoversavefilename() {
		return coversavefilename;
	}
	public void setCoversavefilename(String coversavefilename) {
		this.coversavefilename = coversavefilename;
	}
	public String getProfilesavefilename() {
		return profilesavefilename;
	}
	public void setProfilesavefilename(String profilesavefilename) {
		this.profilesavefilename = profilesavefilename;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getSchool() {
		return school;
	}
	public void setSchool(String school) {
		this.school = school;
	}
	public String getHome() {
		return home;
	}
	public void setHome(String home) {
		this.home = home;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}

}
